package Curs5;

public class ForExemple {

	public static void main(String[] args) {
			//initializare, conditie, iterator
		
		for(int i = 0; i <= 10; i++) {
			
			if(i == 5) {
				
				//break;//iese din bucla
				continue;//face skip la 5
				
				
			}
			
			
			System.out.println(i);
			
		}
				
		
		
		

	}

}
